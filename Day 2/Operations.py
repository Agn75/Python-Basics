x = 6
y = 2
z = 7

# Use of basic math operations such as addition, substraction, multiplication and division
print(f"{x} plus {y} equals = {x+y}")
print(f"{x} minus {y} equals = {x-y}")
print(f"{x} multiplied by {y} equals = {x*y}")
print(f"{x} divided by {y} equals = {x/y}")

# Division without decimals
print(f"{z} divided by {y} without decimals equals to = {z//y}")

# Modulus is the remainder of a non pair division
print(f"{z} modulus of {y} equals to = {z%y}")

# Exponentiation
print(f"{x} raised to the {y} equals to = {x**y}")

