my_list = ["a", "b", "c"]
my_list2 = ["d", "e", "f"]
my_list3 = my_list + my_list2

# Append Method adds a new element to the list
my_list3.append('g')

# Pop method removes the last element on the list unless you add the index as a parameter
my_list3.pop()

# Sort method rearranges the order of the object of the lists
my_list3.sort()

print(my_list3)